import java.util.Scanner;

public class MinusPlus {

    public static void main (String [] args) {
        Scanner in = new Scanner(System.in);

        int minusPlus [] = new int [100];



        for (int i = 0; i < 5; i++) {
            System.out.println("Введи " + (i+1) + " число");
            minusPlus [i] = in.nextInt();
            System.out.println("введенное число minusPlus [" + i + "] = " + minusPlus [i] + " преобразованное введенное число " + (minusPlus [i] * -1));

        }

















    }




























}
